#include "ft_printf.h"

int	ft_isstr(char *str)
{
	size_t	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i] != 0)
		i += ft_ischar(str[i]);
	return (i);
}
